#include <unistd.h>

int	main(int ac, char **av)
{
	char c;

	if (ac == 4)
	{
		while (*av[1])
		{
			c = *av[1]++;
			if (c == *av[2])
				c = *av[3];
			write(1, &c, 1);
		}
	}
	write(1, "\n", 1);
	return (0);
}
